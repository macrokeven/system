package com.letoy.module;

public class Project {
    private String id;
    private String name;
    private String status;
    private String charger_id;
    private String charger_name;
    private String information;


    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

    public String getCharger_id() {
        return charger_id;
    }

    public void setCharger_id(String charger_id) {
        this.charger_id = charger_id;
    }

    public String getCharger_name() {
        return charger_name;
    }

    public void setCharger_name(String charger_name) {
        this.charger_name = charger_name;
    }

    public String getInformation() {
        return information;
    }

    public void setInformation(String information) {
        this.information = information;
    }
}
